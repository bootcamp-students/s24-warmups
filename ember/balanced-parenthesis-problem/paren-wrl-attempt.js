function balancedParens(n) {
    if (n === 0) return ['']
    console.log({ n })
    return loop(n);
}

function loop(n) {
    let output = [wide(n)]

    if (n > 1) {
        let recur = loop(n - 1);
        console.log({ recur });

        let wrapPrevWide = wrap(recur[0]);
        output.push(wrapPrevWide);

        recur.slice(1).forEach(x => {
            output.push(wrap(x));
            output.push(addRight(x));
            output.push(addLeft(x));
        })

        if (n % 2 === 0) {
            let parenDeep = deep(n / 2);
            output.push(parenDeep + parenDeep)
        }

        output = [...new Set(output)]
        console.log({ output, length: output.length })
    }

    return output;
}

function wide(n) {
    return '()'.repeat(n)
}

function wrap(str) {
    return `(${str})`
}

function addRight(str) {
    return str + '()'
}

function addLeft(str) {
    return '()' + str
}

function deep(n) {
    return '('.repeat(n) + ')'.repeat(n)
}

